/***************************************************************
*   k_clear_screen
*       Borra la pantalla en modo texto color.
*
****************************************************************/
void k_clear_screen() 
{
	char *vidmem = (char *)0xb8000;
	unsigned int i = 0;

	while(i < (80*25*2)) {
		vidmem[i]=' ';
		i++;
		vidmem[i] = WHITE_TXT;
		i++;
	};
}


